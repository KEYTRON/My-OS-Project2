// Default font structure
graphics_font_t graphics_default_font = {
    .char_width = 8,
    .char_height = 8,
    .name = "SimpleBitmap8x8",
};

// Initialize font data
static void _init_font(void) {
    static int initialized = 0;
    if (initialized) return;
    initialized = 1;

    // Fill font characters from simple_font_data
    for (int i = 0; i < 95; i++) {
        for (int j = 0; j < 8; j++) {
            graphics_default_font.chars[i + 32].bitmap[j] = simple_font_data[i][j];
        }
    }
}

void graphics_draw_char_at(int32_t x, int32_t y, char c, uint32_t fg_color, uint32_t bg_color) {
    _init_font();

    if (g_graphics_device == NULL) return;

    graphics_font_t *font = &graphics_default_font;
    uint8_t char_idx = (uint8_t)c;

    if (char_idx < 32 || char_idx >= 127) {
        char_idx = 32;  // Use space for unknown characters
    }

    graphics_font_char_t *ch = &font->chars[char_idx];

    // Draw 8x8 bitmap
    for (int row = 0; row < 8; row++) {
        uint8_t bitmap_row = ch->bitmap[row];
        for (int col = 0; col < 8; col++) {
            uint32_t color = (bitmap_row & (1 << (7 - col))) ? fg_color : bg_color;
            graphics_putpixel(x + col, y + row, color);
        }
    }
}

void graphics_draw_string_at(int32_t x, int32_t y, const char *text, uint32_t fg_color, uint32_t bg_color) {
    if (text == NULL) return;

    graphics_font_t *font = &graphics_default_font;
    int32_t current_x = x;

    while (*text) {
        if (*text == '\n') {
            current_x = x;
            y += font->char_height;
        } else if (*text == '\t') {
            current_x += font->char_width * 4;
        } else {
            graphics_draw_char_at(current_x, y, *text, fg_color, bg_color);
            current_x += font->char_width;
        }
        text++;
    }
}

uint8_t graphics_get_font_width(void) {
    _init_font();
    return graphics_default_font.char_width;
}

uint8_t graphics_get_font_height(void) {
    _init_font();
    return graphics_default_font.char_height;
}
